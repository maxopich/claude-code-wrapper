/**
 * Per-agent `comm.md` generator.
 *
 * Each bus-integrated project gets a `comm.md` written to
 * `<projectPath>/.cebab/comm.md` and a single project-relative `@import`
 * line (`@.cebab/comm.md`) appended to the project's CLAUDE.md so the
 * agent picks up the bus protocol description on every claude launch.
 * Keeping the import path internal (relative) is what avoids
 * claude-code's external-import startup trust modal — see `paths.ts`
 * header for the full story.
 *
 * The file is fully derived from inputs (agent name + bus root paths
 * inside the doc), so a re-install can safely overwrite it without
 * losing operator edits — there are no operator edits to lose.
 *
 * Kept intentionally static for v1: it documents the bus *protocol* but does
 * not bake in session-specific context (who the orchestrator is, what chain
 * hop this agent has, etc.). Session-specific framing is delivered via the
 * introduction message at conversation start, where it can vary per run.
 */
import { busBinDir, busLogPath, busInboxDir, busArchiveDir } from './paths.js';

export function renderCommMd(agentName: string): string {
  const bin = busBinDir();
  const inbox = busInboxDir(agentName);
  const archive = busArchiveDir(agentName);
  const log = busLogPath();
  return `<!-- Generated by Cebab. Do not edit by hand; this file is overwritten on
     bus-integration re-install. Edits to the parent CLAUDE.md are preserved. -->

## Bus integration (agent: \`${agentName}\`)

You are the bus agent \`${agentName}\`. When you're part of a multi-agent
conversation started from Cebab's Multi-Agent tab, other agents can send you
messages via the local filesystem bus.

### How messages reach you

Cebab's Stop hook runs after every turn:

    ${bin}/bus-check-inbox.sh ${agentName}

If there are pending messages, they're surfaced as your next instruction
automatically — you don't need to invoke this manually. Your inbox is at
\`${inbox}\` and processed messages move to \`${archive}\`.

### How to send messages

To reply to a single recipient:

    ${bin}/bus-send-msg.sh [--kind <kind>] <recipient> "<text>"

For longer replies, pipe via stdin:

    echo "Long reply..." | ${bin}/bus-send-msg.sh <recipient>

\`<kind>\` is one of: \`intro\`, \`prompt\`, \`reply\`, \`final\`, \`error\`.
Default: \`reply\`. \`<recipient>\` is another agent's slug (e.g.
\`reviewer\`) or the literal \`user\` (orchestrator-only — Cebab intercepts
those and forwards to the browser).

### Conversation discipline

- In **chain mode**, you forward to the next agent in the predetermined
  order. Cebab tells you who that is in the initial prompt of each iteration.
- In **orchestrator mode**, the orchestrator routes for you. Reply only to
  the orchestrator. Do not message workers directly — they won't act on
  out-of-band traffic.
- The \`user\` recipient is reserved for the orchestrator. Workers cannot
  message the user directly; the orchestrator gates that boundary.

### Inspecting state

    ${bin}/bus-status.sh           # inbox depths + recent bus.log events
    cat ${log}                     # full chronological event log
`;
}
