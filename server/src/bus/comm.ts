/**
 * Per-agent `comm.md` generator.
 *
 * In the pure-SDK runtime only the orchestrator still gets a comm.md: it is
 * written to `<orchestratorWorkspace>/.cebab/comm.md` and `@import`ed from
 * the workspace CLAUDE.md (a workspace-relative path, so claude-code's
 * external-import trust modal never fires). Worker projects no longer get a
 * comm.md or any CLAUDE.md/settings.json mutation — their bus protocol is
 * delivered entirely via the per-turn briefing Cebab prepends (see
 * `renderChainBriefing` / `renderRosterPrompt`).
 *
 * The file is fully derived from its single input (agent name), so a
 * re-render can safely overwrite it — there are no operator edits to lose.
 *
 * Kept intentionally static: it documents the bus *protocol* (the `bus_send`
 * tool) but not session-specific context (who the orchestrator is, what
 * chain hop this agent has). That framing is delivered via the introduction
 * message at conversation start, where it can vary per run.
 */
export function renderCommMd(agentName: string): string {
  return `<!-- Generated by Cebab. Do not edit by hand; this file is overwritten
     whenever the workspace is regenerated. -->

## Bus integration (agent: \`${agentName}\`)

You are the bus agent \`${agentName}\`. When you're part of a multi-agent
conversation started from Cebab's Multi-Agent tab, you exchange messages
with the other participants entirely through one in-process tool:
\`bus_send\`. There is no inbox, no shell script, no \`bus.log\`, and no
terminal — Cebab runs each agent as its own \`claude\` session and delivers
turns directly.

### How messages reach you

Cebab delivers every inbound message (a briefing, a peer's reply, a user
prompt the orchestrator routed to you) as your next turn's prompt. You do
not poll or check anything; if it's your turn, the message is in front of
you. When you finish your turn without calling \`bus_send\`, the
conversation simply waits — nothing is lost, but nothing moves either, so
always send before you stop if you owe someone a reply.

### How to send messages

Call the \`bus_send\` tool:

    bus_send(recipient="<slug>", kind="<kind>", text="<your message>")

- \`recipient\` — another agent's slug (e.g. \`reviewer\`), or the literal
  \`user\` (orchestrator-only; Cebab forwards it to the operator's chat) or
  \`_sink\` (fixed-chain mode only; ends the chain).
- \`kind\` — one of \`intro\`, \`prompt\`, \`reply\`, \`final\`. Use
  \`reply\` to hand work back to a peer and \`final\` for a terminal answer
  (to \`user\` in orchestrator mode, to \`_sink\` as the last chain step).
- \`text\` — the full message body (any length; it is a normal tool
  argument, not a shell command, so quoting/escaping is not your problem).

The tool returns \`delivered to <recipient>\` on success, or an error
string you can read and correct (e.g. an unknown recipient) — a rejected
\`bus_send\` does not end your turn, so just fix the arguments and retry.

### Conversation discipline

- In **chain mode** you forward to the next agent in the predetermined
  order. Cebab tells you who that is in your briefing each iteration.
- In **orchestrator mode** the orchestrator routes for you. Reply only to
  the orchestrator; do not message other workers directly — worker→worker
  traffic is dropped.
- The \`user\` recipient is reserved for the orchestrator. Workers cannot
  message the user directly; the orchestrator gates that boundary.
- Anything you write in your turn that is not sent via \`bus_send\` is
  invisible to the other participants and to the operator.
`;
}
